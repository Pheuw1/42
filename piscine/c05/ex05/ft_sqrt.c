int	ft_sqrt(int nb)
{
	int	n;

	if (nb < 0)
		return (0);
	n = nb / 2;
	while ((long) n * n > nb)
		n /= 2;
	while ((long) n * n < nb)
		n++;
	if (n * n == nb)
		return (n);
	return (0);
}
