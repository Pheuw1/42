#include <unistd.h>

void	ft_print_comb(void)
{
	char	n1;
	char	n2;
	char	n3;

	n3 = '0' - 1;
	while (n3++ < '9')
	{
		n2 = n3;
		while (n2++ < '9')
		{
			n1 = n2;
			while (n1++ < '9')
			{
				write(1, &n3, 1);
				write(1, &n2, 1);
				write(1, &n1, 1);
				if (n3 < '7')
					write(1, ", ", 2);
			}
		}
	}
}
