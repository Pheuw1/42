#include <unistd.h>

void	ft_printn(int n)
{
	char	c;

	if (n < 10)
	{
		c = n + '0';
		write(1, "0", 1);
		write(1, &c, 1);
	}
	else
	{
		c = n / 10 + '0';
		write(1, &c, 1);
		c = n % 10 + '0';
		write(1, &c, 1);
	}
}

void	ft_print_comb2(void)
{
	int	n[2];

	n[0] = 0;
	n[1] = 1;
	while (n[0] < 99)
	{
		while (n[1] <= 99)
		{
			ft_printn(n[0]);
			write(1, " ", 1);
			ft_printn(n[1]);
			if (n[0] != 98)
				write(1, ", ", 2);
			n[1]++;
		}
		n[0]++;
		n[1] = n[0] + 1;
	}
}
