static int	uc(char c)
{
	if (c >= 'A' && c <= 'Z')
		return (1);
	return (0);
}

char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((uc(str[i])))
			str[i] += 'a' - 'A';
		i++;
	}
	return (str);
}
